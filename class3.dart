void main() {
  // Equality & Relational Operator

  // EQUAL SYMBOL IS  -->   ==
  num num1 = 34;
  num num2 = 34;
  print(num1 == num2); //true

  // NOT EQUAL TO -->    !=
  num num3 = 34;
  num num4 = 4;
  print(num3 != num4); //true

  // Grater than -->  >

  num num5 = 34;
  num num6 = 4;
  print(num5 > num6); //true

  // Lass than -->  <

  num num7 = 34;
  num num8 = 4;
  print(num7 < num8); //true

// Greater than or equal to   >=
  num num9 = 34;
  num num10 = 4;
  print(num9 >= num10);

  // Less than or equal to  <=
  num num11 = 34;
  num num12 = 4;
  print(num11 <= num12);

  // && oprator -->  &&
  num num13 = 34;
  num num14 = 4;
  num num15 = 34;
  num num16 = 4;
  print(num13 <= num14 && num15 >= num16);

// or oprator --> ||
  num num17 = 34;
  num num18 = 4;
  num num19 = 34;
  num num20 = 4;
  print(num17 <= num18 || num19 >= num20);

  // not oprator  --> !
}
